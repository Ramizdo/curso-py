import math

# Redondeo

print(round(1.3))
print(round(1.7))

# Valor absoluto (Volver positivo)

print(abs(-77))
print(abs(55))

print(math.ceil(1.1))  # Redondear al numero entero hacia arriba
print(math.floor(1.9999))  # Redondear al numero entero hacia abajo
print(math.isnan(23))
print(math.pow(10, 3))  # Elevar un numero
print(math.sqrt(9))  # Raiz cuadrada
